//Loops in C.
/*
 -->Loops are the sequence of instruction that is continually repeated until a certain condition is reached.
 There are two types of loop they are:
 *Entry controlled Loop.
 *Exit controlled Loop.

 1.Entry Controlled Loop.
 -->It checks condition at entry level(at beginning of loop).At first the condition is checked first and then
    after that body of loop is executed. 
    Eg: while and for loop.
  
 2.Exit Control Loop.
  -->It checks the condition at exit lever(at last).The category of loop in which the test 
     condition is checked after the execution of the body of the loop.
     Eg:do-while loop.
  
 

*/